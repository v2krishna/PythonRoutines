print("................................Arithmetic Operators Sample Program .....................")
x = 3
y = 2
print('x + y  = {}'.format(x+y))  # addition
print( 'x - y  = {}'.format(x-y)) # subtraction
print('x * y = {}'.format(x*y))  #multiplication
print('x ** y = {}'.format(x ** y)) #exponential
print('x/y = {}'.format(x/y))   #float division
print('x//y = {}'.format(x//y))  #integer
print('x % y = {}'.format(x % y))  #modulus

